package Day_1;

public class dataTypes {
    public static void main(String[] args) {
        
        // PRIMITIVE DATA TYPE
        // 1byte = 8bit


        byte b = 8;
        System.out.println(b);
       // size of byte = 1byte[-128 to 127] 256

        char ch = 'a';
        System.out.println(ch);
        // size of ch =  2byte


        boolean var = true;
        System.out.println(var);
        // size of boolean = 1byte 


        float price = (float) 10.5;
        System.out.println(price);
        // size of float = 4byte


        short n = 240;
        System.out.println(n);
        // size of short = 2byte 


        int a = 445;
        System.out.println(a);
        // size of int = 4 byte 

        // size of long = 8 byte
       // size of double = 8 byte


    }
    
}
